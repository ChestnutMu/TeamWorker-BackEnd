@Entity
@DynamicUpdate
@DynamicInsert
public class TeamNotification {
    @Id
    @GeneratedValue(generator = "attendanceIdGenerator")
    @GenericGenerator(name = "attendanceIdGenerator", strategy = "com.info.xiaotingtingBackEnd.model.base.IdGenerator")
    @Column(columnDefinition = "char(20)", nullable = false)
    private String teamNotificationId;

    /*团队id*/
    private String teamId;

    /*发送者昵称*/
    private String senderNickname;

    /*发送者头像*/
    private String senderAvatar;

    /*通知标题*/
    private String title;

    /*通知内容*/
    private String content;

    /*发送时间*/
    private Date time;

    /*图片*/
    private String photo;

    public String getTeamNotificationId() {
        return teamNotificationId;
    }

    public void setTeamNotificationId(String teamNotificationId) {
        this.teamNotificationId = teamNotificationId;
    }

    public String getTeamId() {
        return teamId;
    }

    public void setTeamId(String teamId) {
        this.teamId = teamId;
    }

    public String getSenderNickname() {
        return senderNickname;
    }

    public void setSenderNickname(String senderNickname) {
        this.senderNickname = senderNickname;
    }

    public String getSenderAvatar() {
        return senderAvatar;
    }

    public void setSenderAvatar(String senderAvatar) {
        this.senderAvatar = senderAvatar;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

}
